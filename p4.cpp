#include<iostream>

using namespace std;

int main(){
    int m;
    cout<<"Enter month number";
    cin>>m;
    switch (m)
    {
    case 1:
        cout<<"JANUARY";
        break;
    case 2:
    cout<<"FEBRUARY";
    break;
    case 3:
    cout<<"MARCH";
    break;
    
    default:
        break;
    }
}